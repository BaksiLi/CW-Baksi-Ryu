# Decode the Morse code
# https://www.codewars.com/kata/54b724efac3d5402db00065e/


def decodeMorse(text: str):
    return ' '.join([
        ''.join([MORSE_CODE.get(char, '?') for char in word.split()])
        for word in text.lstrip().split('   ')
    ])


